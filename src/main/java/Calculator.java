public class Calculator {
    public Calculator() {
    }

    public static void printSumResult(int firstNum, int secondNum) {
        System.out.println(firstNum + " + " + secondNum + " = " + (firstNum + secondNum));
    }

    public static void printMultiplyResult(int firstNum, int secondNum) {
        System.out.println(firstNum + " * " + secondNum + " = " + (firstNum * secondNum));
    }

}
